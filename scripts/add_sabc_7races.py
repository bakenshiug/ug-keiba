#!/usr/bin/env python3
import json, os

os.chdir(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

def gt(w):
    if w is None: return 'C'
    return 'S' if w>=15 else 'A' if w>=10 else 'B' if w>=7 else 'C'

def gf(p):
    if p is None: return 'C'
    return 'S' if p>=35 else 'A' if p>=25 else 'B' if p>=15 else 'C'

def gpt(g): return {'S':4,'A':3,'B':2,'C':1}.get(g,1)

def update_json(path, sire_stats, trainer_stats, horse_trainer_map):
    """Add sire and trainer SABC to a race-notes JSON."""
    with open(path) as f:
        d = json.load(f)

    # Load sire data from soutei JSON
    date_part = path.split('/')[-1].replace('race-notes/', '').replace('.json', '')
    # e.g. 2026-04-05-hanshin-10r
    parts = date_part.split('-')
    date_str = '-'.join(parts[:3])  # 2026-04-05
    venue_r = '-'.join(parts[3:])   # hanshin-10r
    soutei_path = f'docs/data/soutei-{date_str}.json'

    with open(soutei_path) as f:
        soutei = json.load(f)

    # Build horse→sire map from soutei
    horse_sire_map = {}
    for race in soutei.get('races', []):
        for h in race.get('horses', []):
            horse_sire_map[h['name']] = h.get('sire', '')

    for hname, hdata in d['horses'].items():
        sabc = hdata.setdefault('sabc', {})
        tan = sabc.setdefault('tan', {})
        fuku = sabc.setdefault('fuku', {})

        # Sire
        sire_name = horse_sire_map.get(hname, '')
        sire_stats_entry = sire_stats.get(sire_name, {})
        sg_tan = gt(sire_stats_entry.get('win'))
        sg_fuku = gf(sire_stats_entry.get('place'))
        tan['sire'] = sg_tan
        fuku['sire'] = sg_fuku
        hdata['sabcSire'] = sire_name

        # Trainer
        trainer = horse_trainer_map.get(hname, '')
        tr_entry = trainer_stats.get(trainer, {})
        tg_tan = gt(tr_entry.get('win'))
        tg_fuku = gf(tr_entry.get('place'))
        tan['trainer'] = tg_tan
        fuku['trainer'] = tg_fuku
        hdata['sabcTrainer'] = trainer

        # Recalculate total
        tan['total'] = sum(gpt(v) for k, v in tan.items() if k != 'total')
        fuku['total'] = sum(gpt(v) for k, v in fuku.items() if k != 'total')

    # Rebuild ranks
    d['sabcRankTan'] = sorted(
        [{'name': n, 'total': d['horses'][n]['sabc']['tan']['total']} for n in d['horses']],
        key=lambda x: -x['total']
    )
    d['sabcRankFuku'] = sorted(
        [{'name': n, 'total': d['horses'][n]['sabc']['fuku']['total']} for n in d['horses']],
        key=lambda x: -x['total']
    )

    with open(path, 'w', encoding='utf-8') as f:
        json.dump(d, f, ensure_ascii=False, indent=2)
    print(f'Updated {path}')
    print(f'  Top tan: {d["sabcRankTan"][0]["name"]} {d["sabcRankTan"][0]["total"]}pt')
    print(f'  Top fuku: {d["sabcRankFuku"][0]["name"]} {d["sabcRankFuku"][0]["total"]}pt')

