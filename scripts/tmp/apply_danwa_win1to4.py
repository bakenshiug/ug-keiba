def main():
    for file_name, race_key in FILES.items():
        path = os.path.join(RACE_NOTES_DIR, file_name)
        with open(path) as f:
            d = json.load(f)

        table = DANWA[race_key]
        print(f"\n=== {file_name} ({race_key}) ===")
        hit = 0
        miss = []
        for h in d['horses']:
            name = h['name']
            if name in table:
                t = table[name]
                h['danwaGrade'] = {
                    'grade': t['g'],
                    'reason': t['r'],
                    'fatal': t['f']
                }
                if t['f']:
                    h['danwaGrade']['fatalTag'] = t.get('ft','')
                mark = '🔴' if t['f'] else '—'
                print(f"  {name:18s} → {t['g']} {mark}")
                hit += 1
            else:
                miss.append(name)

        if miss:
            print(f"  ⚠️ 未マッチ: {miss}")

        d['logicVersion'] = 'v2-rel-lap-danwa'
        with open(path, 'w') as f:
            json.dump(d, f, ensure_ascii=False, indent=2)
        print(f"  ✅ {hit}頭に danwaGrade 付与 → {path}")

if __name__ == '__main__':
    main()
